#---------- Repeatation Operator ---------------------
# '*' is treated as repeatation operator
# if we do multiply any string then it repeats that string that number of times.

s='Akash'
print(s*3)
# This operator works different datatye as follows
# List
s=['Akash',10,3,True,None]
print(s*3)
# Tuple
s=('Akash',10,3,True,None)
print(s*3)
