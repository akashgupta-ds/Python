# 2. Iterative Statements
#       a. for loop
#       b. while loop
# if we don't know the iteration in advance then use while loop
# while : execute body as long as some condition is true

# syntax :
# while condition:
#        body

# print no. between 1 to 10
x=0
while x <= 10:
    print(x)
    x+=1

# infinite loop
i=0
while i<100:   # while True: infinite loop
    i=i+1
    print('Hello:',i)


