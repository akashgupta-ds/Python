# --------Flow Control
# In which order statements are going to be executed is decided by flow control statement
# Three types of flow control statements
# 1. Conditional Statements/Selection Statements
#       a. if
#       b. if - else
#       c. if - elif - else

# 2. Iterative Statements
#       a. for loop
#       b. while loop

# 3. Transfer Statements
#       a. break
#       b. continue
#       c. pass


